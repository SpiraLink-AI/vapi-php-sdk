<?php declare(strict_types=1);

namespace Vapi\Assistants\Requests;

use Vapi\Assistants\Types\UpdateAssistantDtoBackgroundSoundZero;
use Vapi\Assistants\Types\UpdateAssistantDtoClientMessagesItem;
use Vapi\Assistants\Types\UpdateAssistantDtoCredentialsItem;
use Vapi\Assistants\Types\UpdateAssistantDtoFirstMessageMode;
use Vapi\Assistants\Types\UpdateAssistantDtoModel;
use Vapi\Assistants\Types\UpdateAssistantDtoServerMessagesItem;
use Vapi\Assistants\Types\UpdateAssistantDtoTranscriber;
use Vapi\Assistants\Types\UpdateAssistantDtoVoice;
use Vapi\Assistants\Types\UpdateAssistantDtoVoicemailDetection;
use Vapi\Core\Json\JsonProperty;
use Vapi\Core\Json\JsonSerializableType;
use Vapi\Core\Types\ArrayType;
use Vapi\Core\Types\Union;
use Vapi\Types\AnalysisPlan;
use Vapi\Types\ArtifactPlan;
use Vapi\Types\BackgroundSpeechDenoisingPlan;
use Vapi\Types\CallHookAssistantSpeechInterrupted;
use Vapi\Types\CallHookCallEnding;
use Vapi\Types\CallHookCustomerSpeechInterrupted;
use Vapi\Types\CallHookCustomerSpeechTimeout;
use Vapi\Types\CompliancePlan;
use Vapi\Types\KeypadInputPlan;
use Vapi\Types\LangfuseObservabilityPlan;
use Vapi\Types\MonitorPlan;
use Vapi\Types\Server;
use Vapi\Types\StartSpeakingPlan;
use Vapi\Types\StopSpeakingPlan;
use Vapi\Types\TransportConfigurationTwilio;

final class UpdateAssistantDto extends JsonSerializableType
{
    /**
     * @var ?UpdateAssistantDtoTranscriber $transcriber These are the options for the assistant's transcriber.
     */
    #[JsonProperty('transcriber')]
    public ?UpdateAssistantDtoTranscriber $transcriber;

    /**
     * @var ?UpdateAssistantDtoModel $model These are the options for the assistant's LLM.
     */
    #[JsonProperty('model')]
    public ?UpdateAssistantDtoModel $model;

    /**
     * @var ?UpdateAssistantDtoVoice $voice These are the options for the assistant's voice.
     */
    #[JsonProperty('voice')]
    public ?UpdateAssistantDtoVoice $voice;

    /**
     * This is the first message that the assistant will say. This can also be a URL to a containerized audio file (mp3, wav, etc.).
     *
     * If unspecified, assistant will wait for user to speak and use the model to respond once they speak.
     *
     * @var ?string $firstMessage
     */
    #[JsonProperty('firstMessage')]
    public ?string $firstMessage;

    /**
     * @var ?bool $firstMessageInterruptionsEnabled
     */
    #[JsonProperty('firstMessageInterruptionsEnabled')]
    public ?bool $firstMessageInterruptionsEnabled;

    /**
     * This is the mode for the first message. Default is 'assistant-speaks-first'.
     *
     * Use:
     * - 'assistant-speaks-first' to have the assistant speak first.
     * - 'assistant-waits-for-user' to have the assistant wait for the user to speak first.
     * - 'assistant-speaks-first-with-model-generated-message' to have the assistant speak first with a message generated by the model based on the conversation state. (`assistant.model.messages` at call start, `call.messages` at squad transfer points).
     *
     * @default 'assistant-speaks-first'
     *
     * @var ?value-of<UpdateAssistantDtoFirstMessageMode> $firstMessageMode
     */
    #[JsonProperty('firstMessageMode')]
    public ?string $firstMessageMode;

    /**
     * These are the settings to configure or disable voicemail detection. Alternatively, voicemail detection can be configured using the model.tools=[VoicemailTool].
     * This uses Twilio's built-in detection while the VoicemailTool relies on the model to detect if a voicemail was reached.
     * You can use neither of them, one of them, or both of them. By default, Twilio built-in detection is enabled while VoicemailTool is not.
     *
     * @var ?UpdateAssistantDtoVoicemailDetection $voicemailDetection
     */
    #[JsonProperty('voicemailDetection')]
    public ?UpdateAssistantDtoVoicemailDetection $voicemailDetection;

    /**
     * @var ?array<value-of<UpdateAssistantDtoClientMessagesItem>> $clientMessages These are the messages that will be sent to your Client SDKs. Default is conversation-update,function-call,hang,model-output,speech-update,status-update,transfer-update,transcript,tool-calls,user-interrupted,voice-input,workflow.node.started. You can check the shape of the messages in ClientMessage schema.
     */
    #[JsonProperty('clientMessages'), ArrayType(['string'])]
    public ?array $clientMessages;

    /**
     * @var ?array<value-of<UpdateAssistantDtoServerMessagesItem>> $serverMessages These are the messages that will be sent to your Server URL. Default is conversation-update,end-of-call-report,function-call,hang,speech-update,status-update,tool-calls,transfer-destination-request,handoff-destination-request,user-interrupted. You can check the shape of the messages in ServerMessage schema.
     */
    #[JsonProperty('serverMessages'), ArrayType(['string'])]
    public ?array $serverMessages;

    /**
     * This is the maximum number of seconds that the call will last. When the call reaches this duration, it will be ended.
     *
     * @default 600 (10 minutes)
     *
     * @var ?float $maxDurationSeconds
     */
    #[JsonProperty('maxDurationSeconds')]
    public ?float $maxDurationSeconds;

    /**
     * This is the background sound in the call. Default for phone calls is 'office' and default for web calls is 'off'.
     * You can also provide a custom sound by providing a URL to an audio file.
     *
     * @var (
     *    value-of<UpdateAssistantDtoBackgroundSoundZero>
     *   |string
     * )|null $backgroundSound
     */
    #[JsonProperty('backgroundSound'), Union('string', 'null')]
    public string|null $backgroundSound;

    /**
     * This determines whether the model's output is used in conversation history rather than the transcription of assistant's speech.
     *
     * Default `false` while in beta.
     *
     * @default false
     *
     * @var ?bool $modelOutputInMessagesEnabled
     */
    #[JsonProperty('modelOutputInMessagesEnabled')]
    public ?bool $modelOutputInMessagesEnabled;

    /**
     * @var ?array<TransportConfigurationTwilio> $transportConfigurations These are the configurations to be passed to the transport providers of assistant's calls, like Twilio. You can store multiple configurations for different transport providers. For a call, only the configuration matching the call transport provider is used.
     */
    #[JsonProperty('transportConfigurations'), ArrayType([TransportConfigurationTwilio::class])]
    public ?array $transportConfigurations;

    /**
     * This is the plan for observability of assistant's calls.
     *
     * Currently, only Langfuse is supported.
     *
     * @var ?LangfuseObservabilityPlan $observabilityPlan
     */
    #[JsonProperty('observabilityPlan')]
    public ?LangfuseObservabilityPlan $observabilityPlan;

    /**
     * @var ?array<UpdateAssistantDtoCredentialsItem> $credentials These are dynamic credentials that will be used for the assistant calls. By default, all the credentials are available for use in the call but you can supplement an additional credentials using this. Dynamic credentials override existing credentials.
     */
    #[JsonProperty('credentials'), ArrayType([UpdateAssistantDtoCredentialsItem::class])]
    public ?array $credentials;

    /**
     * @var ?array<(
     *    CallHookCallEnding
     *   |CallHookAssistantSpeechInterrupted
     *   |CallHookCustomerSpeechInterrupted
     *   |CallHookCustomerSpeechTimeout
     * )> $hooks This is a set of actions that will be performed on certain events.
     */
    #[JsonProperty('hooks'), ArrayType([new Union(CallHookCallEnding::class, CallHookAssistantSpeechInterrupted::class, CallHookCustomerSpeechInterrupted::class, CallHookCustomerSpeechTimeout::class)])]
    public ?array $hooks;

    /**
     * This is the name of the assistant.
     *
     * This is required when you want to transfer between assistants in a call.
     *
     * @var ?string $name
     */
    #[JsonProperty('name')]
    public ?string $name;

    /**
     * This is the message that the assistant will say if the call is forwarded to voicemail.
     *
     * If unspecified, it will hang up.
     *
     * @var ?string $voicemailMessage
     */
    #[JsonProperty('voicemailMessage')]
    public ?string $voicemailMessage;

    /**
     * This is the message that the assistant will say if it ends the call.
     *
     * If unspecified, it will hang up without saying anything.
     *
     * @var ?string $endCallMessage
     */
    #[JsonProperty('endCallMessage')]
    public ?string $endCallMessage;

    /**
     * @var ?array<string> $endCallPhrases This list contains phrases that, if spoken by the assistant, will trigger the call to be hung up. Case insensitive.
     */
    #[JsonProperty('endCallPhrases'), ArrayType(['string'])]
    public ?array $endCallPhrases;

    /**
     * @var ?CompliancePlan $compliancePlan
     */
    #[JsonProperty('compliancePlan')]
    public ?CompliancePlan $compliancePlan;

    /**
     * @var ?array<string, mixed> $metadata This is for metadata you want to store on the assistant.
     */
    #[JsonProperty('metadata'), ArrayType(['string' => 'mixed'])]
    public ?array $metadata;

    /**
     * This enables filtering of noise and background speech while the user is talking.
     *
     * Features:
     * - Smart denoising using Krisp
     * - Fourier denoising
     *
     * Smart denoising can be combined with or used independently of Fourier denoising.
     *
     * Order of precedence:
     * - Smart denoising
     * - Fourier denoising
     *
     * @var ?BackgroundSpeechDenoisingPlan $backgroundSpeechDenoisingPlan
     */
    #[JsonProperty('backgroundSpeechDenoisingPlan')]
    public ?BackgroundSpeechDenoisingPlan $backgroundSpeechDenoisingPlan;

    /**
     * @var ?AnalysisPlan $analysisPlan This is the plan for analysis of assistant's calls. Stored in `call.analysis`.
     */
    #[JsonProperty('analysisPlan')]
    public ?AnalysisPlan $analysisPlan;

    /**
     * @var ?ArtifactPlan $artifactPlan This is the plan for artifacts generated during assistant's calls. Stored in `call.artifact`.
     */
    #[JsonProperty('artifactPlan')]
    public ?ArtifactPlan $artifactPlan;

    /**
     * This is the plan for when the assistant should start talking.
     *
     * You should configure this if you're running into these issues:
     * - The assistant is too slow to start talking after the customer is done speaking.
     * - The assistant is too fast to start talking after the customer is done speaking.
     * - The assistant is so fast that it's actually interrupting the customer.
     *
     * @var ?StartSpeakingPlan $startSpeakingPlan
     */
    #[JsonProperty('startSpeakingPlan')]
    public ?StartSpeakingPlan $startSpeakingPlan;

    /**
     * This is the plan for when assistant should stop talking on customer interruption.
     *
     * You should configure this if you're running into these issues:
     * - The assistant is too slow to recognize customer's interruption.
     * - The assistant is too fast to recognize customer's interruption.
     * - The assistant is getting interrupted by phrases that are just acknowledgments.
     * - The assistant is getting interrupted by background noises.
     * - The assistant is not properly stopping -- it starts talking right after getting interrupted.
     *
     * @var ?StopSpeakingPlan $stopSpeakingPlan
     */
    #[JsonProperty('stopSpeakingPlan')]
    public ?StopSpeakingPlan $stopSpeakingPlan;

    /**
     * This is the plan for real-time monitoring of the assistant's calls.
     *
     * Usage:
     * - To enable live listening of the assistant's calls, set `monitorPlan.listenEnabled` to `true`.
     * - To enable live control of the assistant's calls, set `monitorPlan.controlEnabled` to `true`.
     *
     * @var ?MonitorPlan $monitorPlan
     */
    #[JsonProperty('monitorPlan')]
    public ?MonitorPlan $monitorPlan;

    /**
     * @var ?array<string> $credentialIds These are the credentials that will be used for the assistant calls. By default, all the credentials are available for use in the call but you can provide a subset using this.
     */
    #[JsonProperty('credentialIds'), ArrayType(['string'])]
    public ?array $credentialIds;

    /**
     * This is where Vapi will send webhooks. You can find all webhooks available along with their shape in ServerMessage schema.
     *
     * The order of precedence is:
     *
     * 1. assistant.server.url
     * 2. phoneNumber.serverUrl
     * 3. org.serverUrl
     *
     * @var ?Server $server
     */
    #[JsonProperty('server')]
    public ?Server $server;

    /**
     * @var ?KeypadInputPlan $keypadInputPlan
     */
    #[JsonProperty('keypadInputPlan')]
    public ?KeypadInputPlan $keypadInputPlan;

    /**
     * @param array{
     *   transcriber?: ?UpdateAssistantDtoTranscriber,
     *   model?: ?UpdateAssistantDtoModel,
     *   voice?: ?UpdateAssistantDtoVoice,
     *   firstMessage?: ?string,
     *   firstMessageInterruptionsEnabled?: ?bool,
     *   firstMessageMode?: ?value-of<UpdateAssistantDtoFirstMessageMode>,
     *   voicemailDetection?: ?UpdateAssistantDtoVoicemailDetection,
     *   clientMessages?: ?array<value-of<UpdateAssistantDtoClientMessagesItem>>,
     *   serverMessages?: ?array<value-of<UpdateAssistantDtoServerMessagesItem>>,
     *   maxDurationSeconds?: ?float,
     *   backgroundSound?: (
     *    value-of<UpdateAssistantDtoBackgroundSoundZero>
     *   |string
     * )|null,
     *   modelOutputInMessagesEnabled?: ?bool,
     *   transportConfigurations?: ?array<TransportConfigurationTwilio>,
     *   observabilityPlan?: ?LangfuseObservabilityPlan,
     *   credentials?: ?array<UpdateAssistantDtoCredentialsItem>,
     *   hooks?: ?array<(
     *    CallHookCallEnding
     *   |CallHookAssistantSpeechInterrupted
     *   |CallHookCustomerSpeechInterrupted
     *   |CallHookCustomerSpeechTimeout
     * )>,
     *   name?: ?string,
     *   voicemailMessage?: ?string,
     *   endCallMessage?: ?string,
     *   endCallPhrases?: ?array<string>,
     *   compliancePlan?: ?CompliancePlan,
     *   metadata?: ?array<string, mixed>,
     *   backgroundSpeechDenoisingPlan?: ?BackgroundSpeechDenoisingPlan,
     *   analysisPlan?: ?AnalysisPlan,
     *   artifactPlan?: ?ArtifactPlan,
     *   startSpeakingPlan?: ?StartSpeakingPlan,
     *   stopSpeakingPlan?: ?StopSpeakingPlan,
     *   monitorPlan?: ?MonitorPlan,
     *   credentialIds?: ?array<string>,
     *   server?: ?Server,
     *   keypadInputPlan?: ?KeypadInputPlan,
     * } $values
     */
    public function __construct(
        array $values = [],
    ) {
        $this->transcriber = $values['transcriber'] ?? null;
        $this->model = $values['model'] ?? null;
        $this->voice = $values['voice'] ?? null;
        $this->firstMessage = $values['firstMessage'] ?? null;
        $this->firstMessageInterruptionsEnabled = $values['firstMessageInterruptionsEnabled'] ?? null;
        $this->firstMessageMode = $values['firstMessageMode'] ?? null;
        $this->voicemailDetection = $values['voicemailDetection'] ?? null;
        $this->clientMessages = $values['clientMessages'] ?? null;
        $this->serverMessages = $values['serverMessages'] ?? null;
        $this->maxDurationSeconds = $values['maxDurationSeconds'] ?? null;
        $this->backgroundSound = $values['backgroundSound'] ?? null;
        $this->modelOutputInMessagesEnabled = $values['modelOutputInMessagesEnabled'] ?? null;
        $this->transportConfigurations = $values['transportConfigurations'] ?? null;
        $this->observabilityPlan = $values['observabilityPlan'] ?? null;
        $this->credentials = $values['credentials'] ?? null;
        $this->hooks = $values['hooks'] ?? null;
        $this->name = $values['name'] ?? null;
        $this->voicemailMessage = $values['voicemailMessage'] ?? null;
        $this->endCallMessage = $values['endCallMessage'] ?? null;
        $this->endCallPhrases = $values['endCallPhrases'] ?? null;
        $this->compliancePlan = $values['compliancePlan'] ?? null;
        $this->metadata = $values['metadata'] ?? null;
        $this->backgroundSpeechDenoisingPlan = $values['backgroundSpeechDenoisingPlan'] ?? null;
        $this->analysisPlan = $values['analysisPlan'] ?? null;
        $this->artifactPlan = $values['artifactPlan'] ?? null;
        $this->startSpeakingPlan = $values['startSpeakingPlan'] ?? null;
        $this->stopSpeakingPlan = $values['stopSpeakingPlan'] ?? null;
        $this->monitorPlan = $values['monitorPlan'] ?? null;
        $this->credentialIds = $values['credentialIds'] ?? null;
        $this->server = $values['server'] ?? null;
        $this->keypadInputPlan = $values['keypadInputPlan'] ?? null;
    }
}
